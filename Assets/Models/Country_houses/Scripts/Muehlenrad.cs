using UnityEngine;
using System.Collections;

public class Muehlenrad : MonoBehaviour {
//----------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------

    float zrot;

//----------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------
void Start () 
{
    zrot = 0.0f;    
}
//----------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------
void Update () 
{
    zrot += Time.deltaTime * 50.0f;
    transform.localEulerAngles = new Vector3(0.0f, 0.0f, zrot);
}
//----------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------
}
